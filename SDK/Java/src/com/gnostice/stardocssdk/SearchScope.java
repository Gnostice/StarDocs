package com.gnostice.stardocssdk;

/** 
 Defines the search scope.
*/
public enum SearchScope
{
	/**
	 * Page text.
	 */
	PageText,
	/**
	 * Page images.
	 */
	PageImages,
	/** 
	 Document properties.
	*/
	DocumentProperties,
	/** 
	 Bookmarks.
	*/
	Bookmarks,
	/** 
	 Bookmark actions.
	*/
	BookmarkActions,
	/** 
	 Annotations.
	*/
	Annotations,
	/** 
	 Annotation actions.
	*/
	AnnotationActions;
}